var salary1 = 188.89
var salary2 = 200.98

// ADDITION ( + )

var sum = salary1 + salary2

console.log(sum)
console.log('\n')

// SOUSTRACTION ( - )

var sous = salary1 - salary2

console.log(sous)
console.log('\n')

// Division ( / )

var div = salary1 / salary2

console.log(div)
console.log('\n')

// Multiplication ( * )

var mult = salary1 * salary2

console.log(mult)
console.log('\n')

// Modulo ( % )

var mod = salary1 % 2

console.log(mod)
console.log('\n')

// Arrondi

var arround = Math.round(mod)

console.log(arround)
console.log('\n')

// Valeur Absolue

var abs = Math.abs(salary1 - salary2)

console.log(abs)
console.log('\n')

// Puissance

var pow = Math.pow(salary1, 2)

console.log(pow)

